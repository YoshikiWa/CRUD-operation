    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String Name = txtName.getText();
            String StuID = txtStuID.getText();
            String Start = txtStart.getText();
            String End = txtEnd.getText();
            
            pst = con.prepareStatement("insert into StudyingRoom(StudentName, StudentID, Start, End) values(?,?,?,?)");
            
            pst.setString(1, Name);
            pst.setString(2, StuID);
            pst.setString(3, Start);
            pst.setString(4, End);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Record Added");
                txtName.setText("");
                txtStuID.setText("");
                txtStart.setText("");
                txtEnd.setText("");
                txtName.requestFocus();
                LoadRNo();
                
            }else{
                JOptionPane.showMessageDialog(this, "Record Failed");
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(StudyingRoomFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            String RNo = txtRNo.getSelectedItem().toString();
            pst = con.prepareStatement("select * from StudyingRoom where RNo = ?");
            pst.setString(1,RNo);
            rs = pst.executeQuery();
            
            if(rs.next() == true){
                txtName.setText(rs.getString(2));
                txtStuID.setText(rs.getString(3));
                txtStart.setText(rs.getString(4));
                txtEnd.setText(rs.getString(5));
            }
        } catch (SQLException ex) {
            Logger.getLogger(StudyingRoomFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

