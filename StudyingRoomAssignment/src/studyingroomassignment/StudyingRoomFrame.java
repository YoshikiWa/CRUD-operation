    public void LoadRNo(){
        try {
            pst = con.prepareStatement("select RNo from StudyingRoom");
            rs = pst.executeQuery();
            txtRNo.removeAllItems();
            while(rs.next()){
                txtRNo.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(StudyingRoomFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

